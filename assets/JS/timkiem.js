            function ktTimKiem(query) {
                // Xóa nội dung hiện tại trong bảng
                document.getElementById("myTable").innerHTML = "";
            
                // Tìm sách theo tên (hoặc thông tin tùy loại tìm kiếm của bạn)
                const results = dsSach.filter(sach => {
                    // tìm kiếm sách dựa trên tên sách
                    return sach.ten.toLowerCase().includes(query.toLowerCase());
                });
            
                if (results.length === 0) {
                    document.getElementById("timkiem").innerHTML = "Không tìm thấy kết quả nào.";
                    return false;
                }
            
                // Hiển thị kết quả tìm kiếm
                results.forEach(napSach);
            
                // Xóa thông báo tìm kiếm
                document.getElementById("timkiem").innerHTML = "";
            
                return true;
            }
